package entidades;

public class Quarto {
    
    private int idQuartos;
    private String tipoquarto;
    private String valorquarto;


    public int getIdQuartos () {
        return idQuartos;
    }

    public void setIdQuartos(int idQuartos) {
        this.idQuartos = idQuartos;
    }

    public String getTipoquarto () {
        return tipoquarto;
    }

    public void setTipoquarto (String tipoquarto) {
        this.tipoquarto = tipoquarto;
    }

    public String getValorquarto () {
        return valorquarto;
    }

    public void setValorquarto (String valorquarto) {
        this.valorquarto = valorquarto;
    }

}
